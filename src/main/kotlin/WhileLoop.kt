/*
* Loops can execute a block of code as long as a specified condition is reached.
* Loops are handy because they save time, reduce errors, and they make code more readable.
*/

fun main()
{
    // While Loop
    var i = 0
    while (i < 5) {
        println(i)
        i++
    }

    println("")

    // Do While Loop
    var x = 0
    do {
        println(x)
        x++
    }
    while (x < 8)
}

